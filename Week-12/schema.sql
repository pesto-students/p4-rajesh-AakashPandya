CREATE DATABASE WAREHOUSE;
USE WAREHOUSE;

CREATE TABLE CITIES(CITY CHAR(20) UNIQUE NOT NULL ,STATE CHAR(20) NOT NULL);

CREATE TABLE WAREHOUSES(WID INTEGER PRIMARY KEY, WNAME CHAR(30) NOT NULL , LOCATION_CITY CHAR(20) NOT NULL, FOREIGN KEY(LOCATION_CITY) REFERENCES CITIES(CITY) );

CREATE TABLE STORES (SID INTEGER PRIMARY KEY, STORE_NAME CHAR(20) NOT NULL , LOCATION_CITY CHAR(20) NOT NULL, WID INT NOT NULL, FOREIGN KEY(WID) REFERENCES WAREHOUSES(WID), FOREIGN KEY(LOCATION_CITY) REFERENCES CITIES(CITY) );

CREATE TABLE CUSTOMER(CNO INTEGER PRIMARY KEY, CNAME CHAR(50) NOT NULL , ADDR VARCHAR(50) NOT NULL, CU_CITY CHAR(20) NOT NULL, FOREIGN KEY(CU_CITY) REFERENCES CITIES(CITY)); 

CREATE TABLE ITEMS(ITEMNO INTEGER PRIMARY KEY, DESCRIPTION TEXT NOT NULL, WEIGHT DECIMAL(5,2) NOT NULL , COST DECIMAL(8,2) NOT NULL);

CREATE TABLE ORDERS(ONO INT PRIMARY KEY, ODATE DATE NOT NULL, CNO INT NOT NULL, ITEMNO INT NOT NULL, FOREIGN KEY(CNO) REFERENCES CUSTOMER(CNO), FOREIGN KEY(ITEMNO) REFERENCES ITEMS(ITEMNO));


